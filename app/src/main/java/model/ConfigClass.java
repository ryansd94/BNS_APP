package model;

public class ConfigClass {
    public String androidVersion;
    public String androidURL;
    public String iosVersion;
    public String iosURL;
    public String winformVersion;
    public String winformURL;

    public String getAndroidVersion() {
        return androidVersion;
    }

    public void setAndroidVersion(String androidVersion) {
        this.androidVersion = androidVersion;
    }

    public String getAndroidURL() {
        return androidURL;
    }

    public void setAndroidURL(String androidURL) {
        this.androidURL = androidURL;
    }

    public String getIosVersion() {
        return iosVersion;
    }

    public void setIosVersion(String iosVersion) {
        this.iosVersion = iosVersion;
    }

    public String getIosURL() {
        return iosURL;
    }

    public void setIosURL(String iosURL) {
        this.iosURL = iosURL;
    }

    public String getWinformVersion() {
        return winformVersion;
    }

    public void setWinformVersion(String winformVersion) {
        this.winformVersion = winformVersion;
    }

    public String getWinformURL() {
        return winformURL;
    }

    public void setWinformURL(String winformURL) {
        this.winformURL = winformURL;
    }
}
